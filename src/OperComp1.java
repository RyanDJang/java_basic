public class OperComp1{
    public static void main(String[] args) {

        int num1 = 1;
        int num2 = 2;
        int num3 = 1;
        String str = "Sparta";

        System.out.println(num1 > num2);
        System.out.println(num1 < num2);

        System.out.println(num1 <= num2);
        System.out.println(num1 <= num3);

        System.out.println(num1 >= num2);
        System.out.println(num1 >= num3);

        System.out.println(num1 == num2);
        System.out.println(num1 == num3);

        System.out.println(num1 != num2);
        System.out.println(num1 != num3);

        System.out.println(str.equals("Sparta"));

    }
    }